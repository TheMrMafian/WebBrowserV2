    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String InputURL = jTextFieldURL.getText();
        try {
                jEditorPaneWebPage.setPage(InputURL);
                URL url = new URL (InputURL);
                URLConnection spojeni = url.openConnection();
                StringBuilder inputlines = new StringBuilder();
                BufferedReader in = new BufferedReader(
                        new InputStreamReader(spojeni.getInputStream()));
                String line;
                while ((line = in.readLine()) !=null) {
                inputlines.append(line).append("\n");
            }
            jTextAreaVystup.setText(inputlines.toString());
        } catch (IOException ex) {
            Logger.getLogger(GUIgetURL2.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

